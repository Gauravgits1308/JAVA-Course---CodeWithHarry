/*
1. Relational Operators in Java

Relational operators are used to compare two values.
The result of a relational operation is always boolean (true or false).

Relational Operators:
== → Checks if two values are equal
!= → Checks if two values are not equal
> → Checks if left value is greater than right
< → Checks if left value is smaller than right
>= → Checks if left value is greater than or equal to right
<= → Checks if left value is less than or equal to right


2. Logical Operators in Java

Logical operators are used to combine multiple conditions.

Logical Operators:
&& → Logical AND (true if both conditions are true)
|| → Logical OR (true if at least one condition is true)
! → Logical NOT (reverses the result)
 */