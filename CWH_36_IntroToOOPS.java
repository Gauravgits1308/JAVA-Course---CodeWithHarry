/*
Introduction to OOPs (Object-Oriented Programming)
Object-Oriented Programming (OOP) is a programming paradigm that organizes software using objects and classes.

Key Idea
In OOP, a program is divided into objects that interact with each other.

Basic Concepts of OOP
1. Class
    A class is a blueprint or template for creating objects.
    It defines properties (variables) and behaviors (methods).
    Example : 
    class Car {
    int speed;
    void drive() {
        System.out.println("Car is running");
    }
}

2. Object
    An object is an instance of a class.
    It represents a real-world entity.
    Memory is alloted only after object instantiation.
    Example : 
    Car c1 = new Car();

3. Encapsulation
Wrapping data and methods together into a single unit (class).
Achieved using private variables and public methods.

4. Inheritance
Allows a class to inherit properties and methods from another class.
Promotes code reuse.

5. Polymorphism
One name, many forms.
Same method behaves differently in different situations.
Example: Method Overloading / Method Overriding

6. Abstraction
Hiding internal implementation details and showing only essential features.
Achieved using abstract classes and interfaces.

Key Advantages of OOP
Code reusability
Better organization of code
Easier maintenance
Improved security

How to model a problem in OOPS
Noun        ->  Class       ->  Employee
Adjective   ->  Attribute   ->  name,age,slary
Verb        ->  Methods     ->  getSalary(),increment()

*/